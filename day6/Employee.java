package com.InheritancePractice;

public class Employee {
	private int eid;
	private String ename;
	public Employee() {
		System.out.println("Employee()");
	}
	protected Employee(int eid, String ename) {
		this.eid = eid;
		this.ename = ename;
	}
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	protected void displaye() {
		System.out.println("Eid : "+eid+" Ename : "+ename);
	}

}

